"""# HANDLE MISSING VALUES WITH REASONING"""

# Copy data
df = train_num.copy()

# Example rules:
# LotFrontage -> median (continuous land size)
# GarageYrBlt -> 0 means no garage
# MasVnrArea -> 0 means no masonry veneer
# Others numeric -> median

for col in df.columns:
    if df[col].isnull().sum() > 0:

        if col in ["GarageYrBlt", "GarageArea", "GarageCars"]:
            df[col] = df[col].fillna(0)

        elif col in ["MasVnrArea"]:
            df[col] = df[col].fillna(0)

        else:
            df[col] = df[col].fillna(df[col].median())

"""SECTION 11: FEATURE ENGINEERING"""

df["TotalSF"] = df["1stFlrSF"] + df["2ndFlrSF"] + df["TotalBsmtSF"]

df["HouseAge"] = df["YrSold"] - df["YearBuilt"]

df["YearsSinceRemodel"] = df["YrSold"] - df["YearRemodAdd"]

df["TotalBathrooms"] = (
    df["FullBath"] +
    0.5 * df["HalfBath"] +
    df["BsmtFullBath"] +
    0.5 * df["BsmtHalfBath"]
)

print("Feature Engineering Done")

"""# SECTION 12: MULTICOLLINEARITY CH"""

corr_matrix = df.corr().abs()

upper = corr_matrix.where(
    np.triu(np.ones(corr_matrix.shape), k=1).astype(bool)
)

high_corr = [(column, row, upper.loc[row, column])
             for column in upper.columns
             for row in upper.index
             if upper.loc[row, column] > 0.80]

print("Highly Correlated Pairs (>0.80):\n")
print(high_corr[:15])

"""SECTION 13: PREPARE X AND y"""

X = df.drop("SalePrice", axis=1)
y = train["LogSalePrice"]

"""# SECTION 14: BASELINE LINEAR REGRESSION"""

lr_pipeline = Pipeline([
    ("scaler", StandardScaler()),
    ("model", LinearRegression())
])

kf = KFold(n_splits=5, shuffle=True, random_state=42)

rmse_scores = np.sqrt(
    -cross_val_score(
        lr_pipeline,
        X,
        y,
        scoring="neg_mean_squared_error",
        cv=kf
    )
)

print("Linear Regression RMSE Scores:", rmse_scores)
print("Average RMSE:", rmse_scores.mean())

"""# SECTION 15: RIDGE REGRESSION"""

ridge_pipeline = Pipeline([
    ("scaler", StandardScaler()),
    ("model", Ridge(alpha=10))
])

ridge_scores = np.sqrt(
    -cross_val_score(
        ridge_pipeline,
        X,
        y,
        scoring="neg_mean_squared_error",
        cv=kf
    )
)

print("Ridge RMSE Scores:", ridge_scores)
print("Average Ridge RMSE:", ridge_scores.mean())

""" SECTION 16: FINAL INTERPRETATION"""

if ridge_scores.mean() < rmse_scores.mean():
    print("Ridge improved performance.")
else:
    print("Linear Regression performed better.")

